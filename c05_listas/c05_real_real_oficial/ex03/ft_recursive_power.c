int	ft_recursive_power(int nb, int power)
{
	int	result;

	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	result = 1;
	if (0 < power)
	{
		result = (result * nb) * ft_recursive_power(nb, (power - 1));
	}
	return (result);
}
